public class ModelUsuario {
    
    private int usuId;
    private String usuNome;
    private String usuLogin;
    private String usuSenha;

    /**
     * @return the usuId
     */
    public int getUsuId() {
        return usuId;
    }

    /**
     * @param usuId the usuId to set
     */
    public void setUsuId(int usuId) {
        this.usuId = usuId;
    }

    /**
     * @return the usoNome
     */
    public String getUsuNome() {
        return usuNome;
    }

    /**
     * @param usoNome the usoNome to set
     */
    public void setUsuNome(String usoNome) {
        this.usuNome = usoNome;
    }

    /**
     * @return the usoLogin
     */
    public String getUsuLogin() {
        return usuLogin;
    }

    /**
     * @param usoLogin the usoLogin to set
     */
    public void setUsuLogin(String usoLogin) {
        this.usuLogin = usoLogin;
    }

    /**
     * @return the usoSenha
     */
    public String getUsuSenha() {
        return usuSenha;
    }

    /**
     * @param usoSenha the usoSenha to set
     */
    public void setUsuSenha(String usoSenha) {
        this.usuSenha = usoSenha;
    }
    
    
}
